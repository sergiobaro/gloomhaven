import UIKit

extension UIFont {
  
  static func pirataOne(size: CGFloat) -> UIFont {
    return UIFont(name: "PirataOne-Regular", size: size)!
  }
  
  static func highTower(size: CGFloat) -> UIFont {
    return UIFont(name: "HighTowerText-Reg", size: size)!
  }
  
  static func nyala(size: CGFloat) -> UIFont {
    return UIFont(name: "Nyala", size: size)!
  }
  
}
